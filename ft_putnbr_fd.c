#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	char	digit;

	nbr = (long) n;
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
	{
		digit = nbr + 48;
		write(fd, &digit, 1);
	}
}
